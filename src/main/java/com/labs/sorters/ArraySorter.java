package com.labs.sorters;

/**
 * Abstract class for sorting arrays
 *
 * @author Yurii Krat
 * @version 1.0
 * @since 26.11.16.
 */
public abstract class ArraySorter {

    /**
     * Sorts array of integers
     * @param array array of integers
     */
    public abstract void sort(int[] array);
}
